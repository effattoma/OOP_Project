    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed

        SQLiteDBConnection con = new SQLiteDBConnection();
        Connection c = SQLiteDBConnection.connect();
        String Room = cmbRoom.getSelectedItem().toString();
        try {
            String sql = "SELECT * from bookroom where Room=?;";
            PreparedStatement pst = c.prepareStatement(sql);
            pst.setString(1, Room);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                Room = rs.getString("Room");
                String RoomType = rs.getString("RoomType");
                String Name = rs.getString("Name");
                String Age = rs.getString("Age");
                String Address = rs.getString("Address");
                String Number = rs.getString("Phone");
                String Status = rs.getString("Status");

                Object[] objs = {Name, Age, Address, Number, RoomType};

                DefaultTableModel model = (DefaultTableModel) tblView.getModel();

                model.addRow(objs);
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            System.err.println(e.getClass().getName() + ": " + e.getMessage());
            System.exit(0);
        }
    }//GEN-LAST:event_btnsearchActionPerformed

